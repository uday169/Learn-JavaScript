// for loop and while loop example

var uday = ['Uday', 'Singh', 1997 , 'Developer', true];

console.log('for loop example:');

for(i=0 ; i < uday.length ; i++){
    console.log(uday[i]);
}
//
console.log('while loop example:');
var i = 0;
while(i < uday.length){
    console.log(uday[i]);
    i++;
}